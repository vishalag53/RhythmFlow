import androidx.compose.ui.graphics.Color
import androidx.compose.ui.unit.dp
import com.vishalag53.mp3.music.rhythmflow.stringCapitalized

@Composable
internal fun AboutCard(about: String, aboutInfo: String) {
    Card(
        modifier = Modifier
            .fillMaxWidth(),
        shape = RoundedCornerShape(12.dp),
        colors = CardDefaults.cardColors(
            containerColor = Color(0xFFFDCF9E)
        ),
        elevation = CardDefaults.cardElevation(defaultElevation = 12.dp)
    ) {
        Column(
            modifier = Modifier
                .fillMaxWidth()
                .padding(horizontal = 12.dp, vertical = 8.dp)
        ) {
            Row(
                horizontalArrangement = Arrangement.Center,
                verticalAlignment = Alignment.CenterVertically
            ) {
                Box(
                    modifier = Modifier
                        .width(3.dp)
                        .height(20.dp)
                        .clip(RoundedCornerShape(100.dp))
                        .background(Color(0xFF35363B))
                )
                Spacer(modifier = Modifier.width(8.dp))
                AboutName(stringCapitalized(about))
            }

            Spacer(modifier = Modifier.height(4.dp))

            AboutInfoName(aboutInfo)
        }
    }
}